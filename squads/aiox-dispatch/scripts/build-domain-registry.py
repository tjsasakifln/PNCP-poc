def read_yaml_safe(path: Path) -> dict:
    """Read YAML file, returning empty dict on any error."""
    if not path.exists():
        return {}
    try:
        with open(path, "r", encoding="utf-8") as f:
            content = f.read()
        if HAS_YAML:
            return yaml.safe_load(content) or {}
        # Fallback: extract name/description with regex
        result = {}
        m = re.search(r'^name:\s*(.+)$', content, re.MULTILINE)
        if m:
            result["name"] = m.group(1).strip()
        m = re.search(r'description:\s*[>|]?\s*\n?\s*(.+?)(?:\n\S|\Z)', content, re.DOTALL)
        if m:
            result["description"] = m.group(1).strip()
        return result
    except Exception:
        return {}


def scan_dir_names(directory: Path, ext: str = ".md") -> list[str]:
    """Return sorted list of file stems in a directory."""
    if not directory.exists():
        return []
    return sorted(f.stem for f in directory.glob(f"*{ext}") if f.is_file())


def find_kb_files(data_dir: Path, project_root: Path) -> list[str]:
    """Find knowledge base files (*.md, *.yaml) in a data directory."""
    if not data_dir.exists():
        return []
    kb_files = []
    for ext in ("*.md", "*.yaml"):
        for f in sorted(data_dir.glob(ext)):
            kb_files.append(str(f.relative_to(project_root)).replace("\\", "/"))
    return kb_files


def scan_squads(project_root: Path) -> dict:
    """Scan squads/ directory for all squad domains."""
    squads_dir = project_root / "squads"
    domains = {}

    if not squads_dir.exists():
        return domains

    for squad_dir in sorted(squads_dir.iterdir()):
        if not squad_dir.is_dir():
            continue

        squad_name = squad_dir.name

        # Skip dispatch itself — it's the orchestrator, not a domain
        if squad_name == "dispatch":
            continue

        config = read_yaml_safe(squad_dir / "config.yaml")

        # Discover agents
        agents = scan_dir_names(squad_dir / "agents")

        # Discover tasks
        tasks = scan_dir_names(squad_dir / "tasks")

        # Discover KB files
        kb_files = find_kb_files(squad_dir / "data", project_root)

        # Get entry agent from config
        pack_config = config.get("pack", config)
        entry_agent = config.get("entry_agent", agents[0] if agents else None)

        # Build triggers from known map or derive from agent/task names
        triggers = DOMAIN_KEYWORD_MAP.get(squad_name, {})
        primary_triggers = triggers.get("primary", _derive_keywords(squad_name, agents, tasks))
        secondary_triggers = triggers.get("secondary", [])

        domain_id = _normalize_domain_id(squad_name)

        domains[domain_id] = {
            "source": f"squads/{squad_name}",
            "description": pack_config.get("description", f"Squad: {squad_name}"),
            "triggers": {
                "primary": primary_triggers,
                "secondary": secondary_triggers,
                "weight_primary": 3,
                "weight_secondary": 1,
            },
            "agents": {
                "primary": f"/{squad_name}:agents:{entry_agent}" if entry_agent else None,
                "squad": squad_name,
                "all_agents": [f"/{squad_name}:agents:{a}" for a in agents],
            },
            "kb_files": kb_files[:5],  # Limit to top 5
            "enrichment": ENRICHMENT_DEFAULTS.get(squad_name, "STANDARD"),
            "default_model": MODEL_DEFAULTS.get(squad_name, "haiku"),
            "stats": {
                "agents": len(agents),
                "tasks": len(tasks),
            },
        }

    return domains


def scan_aios_core(project_root: Path) -> dict:
    """Scan .aios-core/ for development agents and create domains."""
    aios_dir = project_root / ".aios-core"
    domains = {}

    if not aios_dir.exists():
        return domains

    # Scan development agents
    agents_dir = aios_dir / "development" / "agents"
    if agents_dir.exists():
        found_agents = scan_dir_names(agents_dir)
    else:
        found_agents = []

    # For each known agent mapping, create a domain entry if agent exists
    for agent_id, config in AIOS_AGENT_DOMAINS.items():
        # Check if agent file exists (some agents may be standalone commands)
        agent_file_exists = agent_id in found_agents or (agents_dir / f"{agent_id}.md").exists()

        # Find context/KB files
        kb_files = config.get("kb_files", [])
        context_file = aios_dir / "context" / f"{agent_id}.md"
        if context_file.exists():
            kb_files = [str(context_file.relative_to(project_root)).replace("\\", "/")] + kb_files

        domain_id = _normalize_domain_id(agent_id)

        domains[domain_id] = {
            "source": ".aios-core",
            "triggers": {
                "primary": config["triggers_primary"],
                "secondary": config["triggers_secondary"],
                "weight_primary": 3,
                "weight_secondary": 1,
            },
            "agents": {
                "primary": f"@{agent_id}",
                "squad": None,
            },
            "kb_files": kb_files,
            "enrichment": config["enrichment"],
            "default_model": config["model"],
        }

        if "note" in config:
            domains[domain_id]["note"] = config["note"]

    return domains


