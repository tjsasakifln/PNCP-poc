# OUTPUT GENERATION
# ─────────────────────────────────────────────────────────────────────────────

def generate_registry_yaml(domains: dict, overrides: dict) -> str:
    """Generate the complete domain-registry.yaml content."""
    lines = [HEADER.format(timestamp=datetime.now().isoformat())]

    # Apply overrides (merge on top of auto-generated)
    for domain_id, override in overrides.items():
        if domain_id in domains:
            # Merge: override takes precedence
            for key, value in override.items():
                domains[domain_id][key] = value
        else:
            # New domain from override
            domains[domain_id] = override

    lines.append("domains:")

    for domain_id in sorted(domains.keys()):
        d = domains[domain_id]
        lines.append(f"  {domain_id}:")

        # Source
        source = d.get("source", "unknown")
        lines.append(f"    source: \"{source}\"")

        # Description (if present)
        if "description" in d:
            desc = d["description"]
            if len(desc) > 80:
                lines.append(f"    description: >-")
                lines.append(f"      {desc}")
            else:
                lines.append(f"    description: \"{desc}\"")

        # Triggers
        triggers = d.get("triggers", {})
        lines.append(f"    triggers:")
        lines.append(f"      primary: {_format_list(triggers.get('primary', []))}")
        lines.append(f"      secondary: {_format_list(triggers.get('secondary', []))}")
        lines.append(f"      weight_primary: {triggers.get('weight_primary', 3)}")
        lines.append(f"      weight_secondary: {triggers.get('weight_secondary', 1)}")

        # Agents
        agents = d.get("agents", {})
        lines.append(f"    agents:")
        primary = agents.get("primary")
        lines.append(f"      primary: \"{primary}\"" if primary else f"      primary: null")
        squad = agents.get("squad")
        lines.append(f"      squad: \"{squad}\"" if squad else f"      squad: null")
        if "all_agents" in agents:
            lines.append(f"      all_agents: {_format_list(agents['all_agents'])}")

        # KB files
        kb_files = d.get("kb_files", [])
        if kb_files:
            lines.append(f"    kb_files:")
            for kb in kb_files:
                lines.append(f"      - \"{kb}\"")

        # Enrichment & model
        lines.append(f"    enrichment: {d.get('enrichment', 'STANDARD')}")
        model = d.get("default_model")
        lines.append(f"    default_model: {model}" if model else f"    default_model: null")

        # Note
        if "note" in d:
            lines.append(f"    note: \"{d['note']}\"")

        # Stats
        if "stats" in d:
            stats = d["stats"]
            lines.append(f"    stats:")
            lines.append(f"      agents: {stats.get('agents', 0)}")
            lines.append(f"      tasks: {stats.get('tasks', 0)}")

        lines.append("")

    # Scoring algorithm (static)
    lines.append("""# ═══════════════════════════════════════════════════════════════════════════════
# DOMAIN SCORING ALGORITHM
# ═══════════════════════════════════════════════════════════════════════════════

scoring:
  algorithm: "weighted_keyword_match"
  tie_breaking:
    - "Prioritize AUTOMATION over MARKETING (execution before creation)"
    - "If still tied, use order of appearance in text"
  multi_domain:
    behavior: "Detect ALL domains, assign tasks to respective agents"
    example: "Story with copy + AC tasks -> copy tasks to copy-chief, AC tasks to ac-automation"

# ═══════════════════════════════════════════════════════════════════════════════
# BUSINESS CONTEXT (Conditional Loading)
# ═══════════════════════════════════════════════════════════════════════════════

business_context:
  triggers: [lançamento, lancamento, abertura, carrinho, live, campanha]
  source_dir: "plan/campaigns/"
  priority_files: [tom-de-voz.md, icp.md, ofertas.md, datas.md]
  max_tokens: 2000
  condition: "Load IF AND ONLY IF triggers present"
""")

    # Summary
    total = len(domains)
    squad_count = sum(1 for d in domains.values() if d.get("agents", {}).get("squad"))
    aios_count = sum(1 for d in domains.values() if d.get("source") == ".aios-core")
    lines.append(f"# Summary: {total} domains ({squad_count} from squads, {aios_count} from .aios-core)")

    return "\n".join(lines)


def _format_list(items: list) -> str:
    """Format a list as YAML inline list."""
    if not items:
        return "[]"
    return "[" + ", ".join(f'"{i}"' for i in items) + "]"


# ─────────────────────────────────────────────────────────────────────────────
# MAIN
# ─────────────────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(
        description="Build domain registry by scanning the project ecosystem"
    )
    parser.add_argument(
        "--output",
        default="squads/dispatch/data/domain-registry.yaml",
        help="Output file path (default: squads/dispatch/data/domain-registry.yaml)",
    )
    parser.add_argument("--dry-run", action="store_true", help="Print output without writing file")
    parser.add_argument("--project-root", default=".", help="Project root directory")
    args = parser.parse_args()

    project_root = Path(args.project_root).resolve()
    print(f"Scanning project: {project_root}")

    # ── Phase 1: Scan all sources ──
    squad_domains = scan_squads(project_root)
    aios_domains = scan_aios_core(project_root)
    org_domain = scan_organization_domain()
    overrides = load_manual_overrides(project_root)

    # ── Phase 2: Merge (squads take precedence over aios-core for same domain) ──
    all_domains = {}
    all_domains.update(aios_domains)
    all_domains.update(org_domain)
    all_domains.update(squad_domains)  # Squads override aios-core if same domain ID

    # ── Phase 3: Validate paths ──
    warnings = validate_paths(all_domains, project_root)
    for w in warnings:
        print(w, file=sys.stderr)

    # ── Phase 4: Generate output ──
    output = generate_registry_yaml(all_domains, overrides)

    # ── Phase 5: Report ──
    total = len(all_domains)
    print(f"Discovered: {len(squad_domains)} squad domains, {len(aios_domains)} aios-core domains, 1 built-in")
    print(f"Total: {total} domains")
    if overrides:
        print(f"Manual overrides applied: {len(overrides)}")
    if warnings:
        print(f"Warnings: {len(warnings)} (broken paths)")

    if args.dry_run:
        print("\n--- DRY RUN ---")
        print(output)
    else:
        output_path = project_root / args.output
        output_path.parent.mkdir(parents=True, exist_ok=True)
        with open(output_path, "w", encoding="utf-8") as f:
            f.write(output)
        print(f"Registry written to: {output_path}")

    # Return exit code
    sys.exit(0)


if __name__ == "__main__":
    main()
