def load_manual_overrides(project_root: Path) -> dict:
    """Load manual overrides from separate file."""
    overrides_path = project_root / "squads" / "dispatch" / "data" / "manual-overrides.yaml"
    if not overrides_path.exists():
        return {}
    return read_yaml_safe(overrides_path).get("overrides", {})


def validate_paths(domains: dict, project_root: Path) -> list[str]:
    """Validate that all referenced paths exist. Return warnings."""
    warnings = []
    for domain_id, domain in domains.items():
        for kb_file in domain.get("kb_files", []):
            full_path = project_root / kb_file
            if not full_path.exists():
                warnings.append(f"[WARN] Domain '{domain_id}': KB file not found: {kb_file}")
    return warnings


# ─────────────────────────────────────────────────────────────────────────────
# HELPERS
# ─────────────────────────────────────────────────────────────────────────────

def _normalize_domain_id(name: str) -> str:
    """Normalize a name into a domain ID (snake_case)."""
    return name.replace("-", "_").lower()


def _derive_keywords(squad_name: str, agents: list[str], tasks: list[str]) -> list[str]:
    """Derive trigger keywords from squad/agent/task names."""
    keywords = [squad_name.replace("-", " ")]
    for name in agents[:3]:
        keywords.append(name.replace("-", " "))
    for name in tasks[:5]:
        clean = name.replace("-", " ").replace("create ", "").replace("build ", "")
        if clean not in keywords:
            keywords.append(clean)
    return keywords[:10]


